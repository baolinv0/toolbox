function mapOldMaskToCurrent = vipblksBrokenLinksMapping()
% Broken links restoration mapping for Computer Vision System Toolbox

% Copyright 2007-2014 The MathWorks, Inc.
%  

mapOldMaskToCurrent = ...
    cat(1, ...
    dspvisionMap, ... 
    vipobslibMap, ...             % obsolete library
    vipanalysisMap,...            % obsolete library
    vipmiscMap, ...
    visionanalysisMap, ...
    visionsourcesMap, ...
    visionutilitiesMap, ...
    visionconversionsMap, ...
    visiongeotformsMap, ...
    visionmorphopsMap, ...
    visionstatisticsMap, ...
    visionfilterMap, ...
    visiontextngfixMap, ...
    visionsinksMap, ...
    visiontransformsMap);

% Note: viputilities, vipgeotforms, etc. are not included in the mapping
%       since they contain blocks with names identical to the vision*
%       equivalents.  That means that if a link to an older block from
%       vip* libray is broken, it will be restored to its vision* version.
%
%       vipanalysis is an exception because it holds Trace Boundaries while
%       visionanalysis has Trace Boundary (different name).  Hence we have
%       two separate mappings for these libraries that cover Trace Boundary
%       explicitly

mapOldMaskToCurrent = cell2struct( mapOldMaskToCurrent, ...
    { 'oldMaskType','newMaskType','newBlockType','newRefBlock', ...
      'MaskNames', 'SFunctionName' }, 2);


